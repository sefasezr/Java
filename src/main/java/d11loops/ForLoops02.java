package d11loops;

public class ForLoops02 {
    public static void main(String[] args) {

        for (int i = 1; i <5 ; i++)
        {
            System.out.println("Week: "+i);
            for (int j = 1; j <8 ; j++){
                System.out.println("Day: "+j);
            }

        }
    }
}
